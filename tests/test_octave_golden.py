"""Golden tests comparing Python RSP pipeline against GNU Octave (MATLAB) reference outputs.

These tests validate that the Python port produces results consistent with the
original MATLAB implementation by Corman & Crane, run through GNU Octave 10.3.0.

The Octave reference data was generated by run_octave_validation.m using these configs:
  - pig:         curvature cross field, alignment energy, boundary=true, hardedge=false
  - B36:         smooth cross field, distortion energy, boundary=true, hardedge=true
  - SquareMyles: trivial connection, chebyshev energy, boundary=true, hardedge=false

Key differences between Python and Octave outputs:
  - Cross fields from eigensolver (smooth/curvature) may differ due to eigenvector sign
    ambiguity, but produce equivalent parameterizations (same singularity structure,
    same convergence, zero flipped triangles).
  - Trivial connection is deterministic, so SquareMyles should produce closer results.
  - Cut mesh topology can differ (different seam placement), so Xp vertex counts and
    exact UV coordinates may not match. We validate structural properties instead.
"""

