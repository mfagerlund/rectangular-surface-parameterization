class TestPigCurvatureAlignment:
    """Pig mesh with curvature cross field and alignment energy.

    Octave reference: 0 flipped, 8 iterations, 10+/30- singularities.
    Cross field uses eigensolver so exact values may differ, but singularity
    structure and convergence properties should match.
    """

    @pytest.fixture(scope="class")
    def pipeline_result(self):
        Src = load_and_rescale("pig")
        dec = dec_tri(Src)
        param, Src, dec = preprocess_ortho_param(Src, dec, True, False, 40)

        omega, ang, sing, kappa, Curv = compute_curvature_cross_field(
            Src, param, dec, 30, 1e-1
        )
        aspect_ratio = (np.abs(kappa[:, 0]) + 1e-5) / (np.abs(kappa[:, 1]) + 1e-5)
        t = np.exp(5)
        aspect_ratio = np.clip(aspect_ratio, 1 / t, t)

        weight = EnergyWeight(
            w_gradv=1e-2, w_ang=1, w_ratio=1,
            aspect_ratio=aspect_ratio, ang_dir=ang
        )

        if len(param.ide_bound) > 0:
            Edge_jump, v2t, base_tri = reduce_corner_var_2d_cut(Src, param.ide_bound)
        else:
            Edge_jump, v2t, base_tri = reduce_corner_var_2d(Src)
        k21, Reduction = reduction_from_ff2d(Src, param, ang, omega, Edge_jump, v2t)

        u = np.zeros(Src.num_vertices)
        v = np.zeros(Src.num_vertices)
        result = optimize_RSP(
            omega, ang, u, v, Src, param, dec, Reduction,
            "alignment", weight, False, 200
        )

        disk_mesh, dec_cut, Align, Rot = mesh_to_disk_seamless(
            Src, param, result.angn, sing, k21, True, True, False
        )
        Xp, dX = parametrization_from_scales(
            Src, disk_mesh, dec_cut, param, result.angn, result.om,
            result.ut, result.vt, Align, Rot
        )
        disto = extract_scale_from_param(
            Xp, Src.vertices, Src.triangles, param,
            disk_mesh.triangles, result.angn
        )[0]

        return {
            "Src": Src, "sing": sing, "result": result,
            "Xp": Xp, "disto": disto, "param": param,
        }

    def test_mesh_dimensions(self, pipeline_result):
        Src = pipeline_result["Src"]
        golden = OCTAVE_GOLDEN["pig"]
        assert Src.num_vertices == golden["num_vertices"]
        assert Src.num_faces == golden["num_faces"]
        assert Src.num_edges == golden["num_edges"]

    def test_singularity_counts(self, pipeline_result):
        sing = pipeline_result["sing"]
        golden = OCTAVE_GOLDEN["pig"]
        assert int(np.sum(sing > 1 / 8)) == golden["sing_pos"]
        assert int(np.sum(sing < -1 / 8)) == golden["sing_neg"]

    def test_zero_flipped_triangles(self, pipeline_result):
        disto = pipeline_result["disto"]
        assert np.sum(disto.detJ <= 0) == OCTAVE_GOLDEN["pig"]["flipped"]

    def test_convergence(self, pipeline_result):
        assert pipeline_result["result"].flag == 0

    def test_uv_range_reasonable(self, pipeline_result):
        """UV coordinates should span a reasonable range (not collapsed or exploded)."""
        Xp = pipeline_result["Xp"]
        u_range = Xp[:, 0].max() - Xp[:, 0].min()
        v_range = Xp[:, 1].max() - Xp[:, 1].min()
        assert 0.1 < u_range < 10.0
        assert 0.1 < v_range < 10.0


