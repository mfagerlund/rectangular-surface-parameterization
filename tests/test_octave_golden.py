MESH_DIR = Path(__file__).parent.parent / "Mesh"


@dataclass
class EnergyWeight:
    w_conf_ar: float = 0.0
    w_gradv: float = 1e-2
    w_ang: Optional[float] = None
    w_ratio: Optional[float] = None
    aspect_ratio: Optional[np.ndarray] = None
    ang_dir: Optional[np.ndarray] = None


def load_and_rescale(mesh_name):
    """Load mesh and rescale to unit area (matching MATLAB pipeline)."""
    X, T = readOBJ(str(MESH_DIR / f"{mesh_name}.obj"))[:2]
    cross_prod = np.cross(X[T[:, 0]] - X[T[:, 1]], X[T[:, 0]] - X[T[:, 2]])
    area_tot = np.sum(np.sqrt(np.sum(cross_prod ** 2, axis=1))) / 2
    X = X / np.sqrt(area_tot)
    return mesh_info(X, T)


